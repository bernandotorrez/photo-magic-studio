export function isDisposableEmail(email: string): boolean {
  const domain = email.split('@')[1]?.toLowerCase();
  if (!domain) return true;
  
  return DISPOSABLE_EMAIL_DOMAINS.includes(domain);
}

export function isValidEmailProvider(email: string): boolean {
  const domain = email.split('@')[1]?.toLowerCase();
  if (!domain) return false;
  
  // If it's a disposable email, reject it
  if (isDisposableEmail(email)) {
    return false;
  }
  
  // Allow known providers
  if (ALLOWED_EMAIL_PROVIDERS.includes(domain)) {
    return true;
  }
  
  // Allow corporate/custom domains (not in disposable list)
  // Corporate emails typically have custom domains
  return true;
}
